const morseAlphabetReversed: { [key: string]: string } = Object.entries(morseAlphabet).reduce((acc, [key, value]) => ({ ...acc, [value]: key }), {});

export function convertToMC(text: string): string {
  return text.toUpperCase().split('').map(char => morseAlphabet[char]).join(' ');
}

export function convertFromMC(code: string): string {
  return code.split(/\s/gi).map(codePattern => morseAlphabetReversed[codePattern]).join('');
}
